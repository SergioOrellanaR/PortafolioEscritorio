    private void btnEditarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarDatosActionPerformed
        
        if(!estaEditando){
            estaEditando = true;
            txtRUTProveedor.setEditable(true);
            txtRutRepresentante.setEditable(true);
            txtEmail.setEditable(true);
            txtTelefono.setEditable(true);
            txtNombreRepresentante.setEditable(true);
            cboDvProveedor.setEnabled(true);
            cboDvRepresentante.setEnabled(true);
            cboRubroProveedor.setEnabled(true);
            btnCancelarEdicion.setEnabled(true);
            txtNombreProveedor.setEditable(true);
        }
        
        if(estaRegistrando){
            JOptionPane.showMessageDialog(rootPane, new Registro().registrarProveedorBD(txtNombreProveedor.getText(), Integer.parseInt(txtRUTProveedor.getText()), String.valueOf(cboDvProveedor.getSelectedItem()).charAt(0), Integer.parseInt(txtTelefono.getText()), txtEmail.getText(), txtNombreRepresentante.getText(), Integer.parseInt(txtRutRepresentante.getText()), String.valueOf(cboDvRepresentante.getSelectedItem()).charAt(0), String.valueOf(cboRubroProveedor.getSelectedItem())), "Registro de proveedor", HEIGHT);
            actualizarListaFuncionarios();
        }
    }//GEN-LAST:event_btnEditarDatosActionPerformed

