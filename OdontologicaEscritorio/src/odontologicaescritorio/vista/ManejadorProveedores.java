    public void actualizarListaFuncionarios(){
        listaProveedores = new Lista().listarProveedores();
        String[] columnas = {"RUT","Nombres", "Apellidos"};
        DefaultTableModel modeloTabla = new DefaultTableModel(columnas, 0);
        
        for(ProveedorDTO proveedor : listaProveedores){
            String rut      = proveedor.getRutEmpresa() + "-" + proveedor.getDvEmpresa();
            String nombre   = proveedor.getNombreEmpresa();
            int telefono = proveedor.getTelefono();
            Object[] elemento = {rut, nombre, telefono};
            modeloTabla.addRow(elemento);
        };
        tblProveedores.setModel(modeloTabla);        
    };

